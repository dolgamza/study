function isMobile(){
    var filter = "win16|win32|win64|mac";
    if(navigator.platform){
        return (filter.indexOf(navigator.platform.toLowerCase()) < 0) ? true : false;
    }
}

$(document).ready(function() {
	$(".onlymobile").click(function(e){
		if (!isMobile()) {
			alert("웹환경에서는 이용하실 수 없습니다.");
			e.preventDefault();
			e.stopPropagation();
		}
	});
	
});